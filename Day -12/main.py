# Strings Slicing and Operations on Strings in python

my_name = "Kabir,Harry"
print(my_name[0:5])  # Slicing from index 0 to 4

# Length of a string
print("Length of a string is :",len(my_name))  # Output: 11
fruit = "Mango"
print(fruit[0:4]) # Slicing from index 0 to 3
print(fruit[-4:0]) # Slicing using negative indexing from -4 to 0
print(fruit[::2]) # Slicing with step of 2

